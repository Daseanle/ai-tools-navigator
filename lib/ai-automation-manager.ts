  private async establishBaseline(): Promise<void> {
    console.log('📊 建立性能基线...')
    
    this.performanceBaseline = await this.collectMetrics()
    console.log('✅ 性能基线已建立', this.performanceBaseline)
  }

  private async startAutomationLoop(): Promise<void> {
    console.log('🔄 开始自动化循环...')
    
    while (this.isRunning) {
      try {
        // 主要自动化循环
        await this.executeAutomationCycle()
        
        // 等待下一个循环
        await this.sleep(30 * 60 * 1000) // 30分钟
      } catch (error) {
        console.error('自动化循环出错:', error)
        await this.sleep(5 * 60 * 1000) // 出错后等待5分钟
      }
    }
  }

