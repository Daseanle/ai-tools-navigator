  private async learnAndAdapt(): Promise<void> {
    console.log('🧠 学习和适应...')
    
    // 评估已执行策略的效果
    const activeStrategies = Array.from(this.activeStrategies)
    
    for (const strategyId of activeStrategies) {
      const strategy = this.strategies.get(strategyId)
      if (strategy) {
        const success = await this.evaluateStrategySuccess(strategy)
        
        if (success) {
          console.log(`✅ 策略"${strategy.name}"执行成功`)
        } else {
          console.log(`❌ 策略"${strategy.name}"未达到预期效果`)
          this.activeStrategies.delete(strategyId)
          
          // 检查是否需要回滚
          await this.checkRollbackConditions(strategy)
        }
      }
    }
    
    // 学习用户行为模式
    await this.learnUserPatterns()
    
    // 优化算法参数
    await this.optimizeAlgorithmParameters()
  }

  private async evaluateStrategySuccess(strategy: AutomationStrategy): Promise<boolean> {
    // 检查成功指标
    for (const metric of strategy.successMetrics) {
      const met = await this.checkMetric(metric)
      if (!met) return false
    }
    return true
  }

  private async checkRollbackConditions(strategy: AutomationStrategy): Promise<void> {
    for (const condition of strategy.rollbackConditions) {
      const shouldRollback = await this.checkMetric(condition)
      if (shouldRollback) {
        console.log(`🔄 执行策略回滚: ${strategy.name}`)
        await this.rollbackStrategy(strategy)
        break
      }
    }
  }

